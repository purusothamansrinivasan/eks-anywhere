func DeletefargateprofileHandler(cfg *config.APIConfig) func(ctx context.Context, request mcp.CallToolRequest) (*mcp.CallToolResult, error) {
	return func(ctx context.Context, request mcp.CallToolRequest) (*mcp.CallToolResult, error) {
		args, ok := request.Params.Arguments.(map[string]any)
		if !ok {
			return mcp.NewToolResultError("Invalid arguments object"), nil
		}
		nameVal, ok := args["name"]
		if !ok {
			return mcp.NewToolResultError("Missing required path parameter: name"), nil
		}
		name, ok := nameVal.(string)
		if !ok {
			return mcp.NewToolResultError("Invalid path parameter: name"), nil
		}
		fargateProfileNameVal, ok := args["fargateProfileName"]
		if !ok {
			return mcp.NewToolResultError("Missing required path parameter: fargateProfileName"), nil
		}
		fargateProfileName, ok := fargateProfileNameVal.(string)
		if !ok {
			return mcp.NewToolResultError("Invalid path parameter: fargateProfileName"), nil
		}
		url := fmt.Sprintf("%s/clusters/%s/fargate-profiles/%s", cfg.BaseURL, name, fargateProfileName)
		req, err := http.NewRequest("DELETE", url, nil)
		if err != nil {
			return mcp.NewToolResultErrorFromErr("Failed to create request", err), nil
		}
		// Set authentication based on auth type
		// Handle multiple authentication parameters
		if cfg.BearerToken != "" {
			req.Header.Set("X-Amz-Security-Token", cfg.BearerToken)
		}
		req.Header.Set("Accept", "application/json")

		resp, err := http.DefaultClient.Do(req)
		if err != nil {
			return mcp.NewToolResultErrorFromErr("Request failed", err), nil
		}
		defer resp.Body.Close()

		body, err := io.ReadAll(resp.Body)
		if err != nil {
			return mcp.NewToolResultErrorFromErr("Failed to read response body", err), nil
		}

		if resp.StatusCode >= 400 {
			return mcp.NewToolResultError(fmt.Sprintf("API error: %s", body)), nil
		}
		// Use properly typed response
		var result models.DeleteFargateProfileResponse
		if err := json.Unmarshal(body, &result); err != nil {
			// Fallback to raw text if unmarshaling fails
			return mcp.NewToolResultText(string(body)), nil
		}

		prettyJSON, err := json.MarshalIndent(result, "", "  ")
		if err != nil {
			return mcp.NewToolResultErrorFromErr("Failed to format JSON", err), nil
		}

		return mcp.NewToolResultText(string(prettyJSON)), nil
	}
}

func CreateDeletefargateprofileTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("delete_clusters_name_fargate-profiles_fargateProfileName",
		mcp.WithDescription("<p>Deletes an Fargate profile.</p> <p>When you delete a Fargate profile, any pods running on Fargate that were created with the profile are deleted. If those pods match another Fargate profile, then they are scheduled on Fargate with that profile. If they no longer match any Fargate profiles, then they are not scheduled on Fargate and they may remain in a pending state.</p> <p>Only one Fargate profile in a cluster can be in the <code>DELETING</code> status at a time. You must wait for a Fargate profile to finish deleting before you can delete any other profiles in that cluster.</p>"),
		mcp.WithString("name", mcp.Required(), mcp.Description("The name of the Amazon EKS cluster associated with the Fargate profile to delete.")),
		mcp.WithString("fargateProfileName", mcp.Required(), mcp.Description("The name of the Fargate profile to delete.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    DeletefargateprofileHandler(cfg),
	}
}
