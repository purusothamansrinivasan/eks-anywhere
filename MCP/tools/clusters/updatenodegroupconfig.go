func CreateUpdatenodegroupconfigTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_clusters_name_node-groups_nodegroupName_update-config",
		mcp.WithDescription("Updates an Amazon EKS managed node group configuration. Your node group continues to function during the update. The response output includes an update ID that you can use to track the status of your node group update with the <a>DescribeUpdate</a> API operation. Currently you can update the Kubernetes labels for a node group or the scaling configuration."),
		mcp.WithString("name", mcp.Required(), mcp.Description("The name of the Amazon EKS cluster that the managed node group resides in.")),
		mcp.WithString("nodegroupName", mcp.Required(), mcp.Description("The name of the managed node group to update.")),
		mcp.WithObject("taints", mcp.Description("Input parameter: An object representing the details of an update to a taints payload. For more information, see <a href=\"https://docs.aws.amazon.com/eks/latest/userguide/node-taints-managed-node-groups.html\">Node taints on managed node groups</a>.")),
		mcp.WithObject("updateConfig", mcp.Description("Input parameter: The node group update configuration.")),
		mcp.WithString("clientRequestToken", mcp.Description("Input parameter: Unique, case-sensitive identifier that you provide to ensure the idempotency of the request.")),
		mcp.WithObject("labels", mcp.Description("Input parameter: An object representing a Kubernetes label change for a managed node group.")),
		mcp.WithObject("scalingConfig", mcp.Description("Input parameter: An object representing the scaling configuration details for the Auto Scaling group that is associated with your node group. When creating a node group, you must specify all or none of the properties. When updating a node group, you can specify any or none of the properties.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    UpdatenodegroupconfigHandler(cfg),
	}
}
